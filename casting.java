public class casting {

    public static void main(String[] args){
        
        //implicit casting

        double price = 100.00;
        double finalprice = price + 18.43;
        System.out.println(finalprice);

        //Explicit casting

        int prize = (int)100.87;
        int fp = prize + (int)18.43;
        System.out.println((int)fp);
    }
    
}
